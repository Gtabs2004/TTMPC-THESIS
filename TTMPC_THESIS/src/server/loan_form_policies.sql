-- Co-maker schema support for both members and non-members.
ALTER TABLE IF EXISTS public.co_makers ADD COLUMN IF NOT EXISTS id BIGINT GENERATED BY DEFAULT AS IDENTITY;
ALTER TABLE IF EXISTS public.co_makers ADD COLUMN IF NOT EXISTS co_maker_name TEXT;
