DO $$
DECLARE
  existing_pk_name text;
BEGIN
  IF to_regclass('public.co_makers') IS NOT NULL THEN
    -- Drop any existing primary key (name can vary: co_makers_pk / co_makers_pkey / etc.).
    SELECT conname
    INTO existing_pk_name
    FROM pg_constraint
    WHERE conrelid = 'public.co_makers'::regclass
      AND contype = 'p'
    LIMIT 1;

    IF existing_pk_name IS NOT NULL THEN
      EXECUTE format('ALTER TABLE public.co_makers DROP CONSTRAINT %I', existing_pk_name);
    END IF;

    -- Backfill id for existing rows before creating new PK.
    EXECUTE 'UPDATE public.co_makers SET id = DEFAULT WHERE id IS NULL';

    IF NOT EXISTS (
      SELECT 1
      FROM pg_constraint
      WHERE conname = 'co_makers_id_pk'
        AND conrelid = 'public.co_makers'::regclass
    ) THEN
      EXECUTE 'ALTER TABLE public.co_makers ADD CONSTRAINT co_makers_id_pk PRIMARY KEY (id)';
    END IF;

    IF EXISTS (
      SELECT 1
      FROM information_schema.columns
      WHERE table_schema = 'public'
        AND table_name = 'co_makers'
        AND column_name = 'member_id'
        AND is_nullable = 'NO'
    ) THEN
      EXECUTE 'ALTER TABLE public.co_makers ALTER COLUMN member_id DROP NOT NULL';
    END IF;
  END IF;
END $$;

