      CREATE POLICY "service role all loans"
      ON loans
      FOR ALL
      USING (auth.role() = ''service_role'')
      WITH CHECK (auth.role() = ''service_role'')
    ';
  END IF;

  IF to_regclass('public.co_makers') IS NOT NULL THEN
    EXECUTE 'DROP POLICY IF EXISTS "authenticated insert co makers for own loan" ON co_makers';
    EXECUTE '
      CREATE POLICY "authenticated insert co makers for own loan"
      ON co_makers
      FOR INSERT
      TO authenticated
      WITH CHECK (
        EXISTS (
          SELECT 1
          FROM public.loans l
          WHERE l.control_number = co_makers.loan_id
            AND (lower(coalesce(l.user_email, '''')) = lower(auth.email()) OR l.member_id = auth.uid())
        )
      )
    ';

    EXECUTE 'DROP POLICY IF EXISTS "authenticated read co makers for own loan" ON co_makers';
    EXECUTE '
      CREATE POLICY "authenticated read co makers for own loan"
      ON co_makers
      FOR SELECT
      TO authenticated
      USING (
        EXISTS (
          SELECT 1
          FROM public.loans l
          WHERE l.control_number = co_makers.loan_id
            AND (lower(coalesce(l.user_email, '''')) = lower(auth.email()) OR l.member_id = auth.uid())
        )
      )
    ';

    EXECUTE 'DROP POLICY IF EXISTS "service role all co makers" ON co_makers';
    EXECUTE '
      CREATE POLICY "service role all co makers"
      ON co_makers
      FOR ALL
      USING (auth.role() = ''service_role'')
      WITH CHECK (auth.role() = ''service_role'')
    ';
  END IF;
END $$;

