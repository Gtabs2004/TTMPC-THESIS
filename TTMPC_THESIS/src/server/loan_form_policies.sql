CREATE OR REPLACE FUNCTION public.has_portal_role(
  p_user_id uuid,
  p_email text,
  p_roles text[]
)
RETURNS boolean
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  matched boolean := false;
BEGIN
  IF to_regclass('public.member_account') IS NOT NULL THEN
    EXECUTE '
      SELECT EXISTS (
        SELECT 1
        FROM public.member_account ma
        WHERE (
          ma.user_id = $1
          OR lower(coalesce(ma.email, '''')) = lower(coalesce($2, ''''))
        )
          AND lower(coalesce(ma.role, '''')) = ANY ($3)
      )
    '
    INTO matched
    USING p_user_id, p_email, p_roles;

    IF matched THEN
      RETURN true;
    END IF;
  END IF;

  IF to_regclass('public.member_accounts') IS NOT NULL THEN
    EXECUTE '
      SELECT EXISTS (
        SELECT 1
        FROM public.member_accounts ma
        WHERE (
          ma.user_id = $1
          OR lower(coalesce(ma.email, '''')) = lower(coalesce($2, ''''))
        )
          AND lower(coalesce(ma.role, '''')) = ANY ($3)
      )
    '
    INTO matched
    USING p_user_id, p_email, p_roles;

    IF matched THEN
      RETURN true;
    END IF;
  END IF;

  RETURN false;
END;
$$;

