DO $$
BEGIN
  IF to_regclass('public.loan_types') IS NOT NULL THEN
    IF NOT EXISTS (SELECT 1 FROM public.loan_types WHERE lower(coalesce(name, '')) IN ('bonus', 'bonus loan')) THEN
      INSERT INTO public.loan_types (code, name) VALUES ('BONUS', 'Bonus Loan');
    END IF;

    IF NOT EXISTS (SELECT 1 FROM public.loan_types WHERE lower(coalesce(name, '')) IN ('consolidated', 'consolidated loan')) THEN
      INSERT INTO public.loan_types (code, name) VALUES ('CONSOLIDATED', 'Consolidated Loan');
    END IF;

    IF NOT EXISTS (SELECT 1 FROM public.loan_types WHERE lower(coalesce(name, '')) IN ('emergency', 'emergency loan')) THEN
      INSERT INTO public.loan_types (code, name) VALUES ('EMERGENCY', 'Emergency Loan');
    END IF;

    IF NOT EXISTS (SELECT 1 FROM public.loan_types WHERE lower(coalesce(name, '')) IN ('koica', 'koica loan', 'agri-business financial facility loan', 'abff loan')) THEN
      INSERT INTO public.loan_types (code, name) VALUES ('KOICA', 'ABFF Loan');
    END IF;

    IF NOT EXISTS (SELECT 1 FROM public.loan_types WHERE lower(coalesce(name, '')) IN ('nonmember bonus loan', 'non-member bonus loan', 'non member bonus loan')) THEN
      INSERT INTO public.loan_types (code, name) VALUES ('NONMEMBER_BONUS', 'Nonmember Bonus Loan');
    END IF;

    UPDATE public.loan_types
    SET code = 'BONUS'
    WHERE lower(coalesce(name, '')) IN ('bonus', 'bonus loan')
      AND (code IS NULL OR btrim(code) = '');

    UPDATE public.loan_types
    SET code = 'CONSOLIDATED'
    WHERE lower(coalesce(name, '')) IN ('consolidated', 'consolidated loan')
      AND (code IS NULL OR btrim(code) = '');

    UPDATE public.loan_types
    SET code = 'EMERGENCY'
    WHERE lower(coalesce(name, '')) IN ('emergency', 'emergency loan')
      AND (code IS NULL OR btrim(code) = '');

    UPDATE public.loan_types
    SET code = 'KOICA'
    WHERE lower(coalesce(name, '')) IN ('koica', 'koica loan', 'agri-business financial facility loan', 'abff loan')
      AND (code IS NULL OR btrim(code) = '');

    UPDATE public.loan_types
    SET code = 'NONMEMBER_BONUS'
    WHERE lower(coalesce(name, '')) IN ('nonmember bonus loan', 'non-member bonus loan', 'non member bonus loan')
      AND (code IS NULL OR btrim(code) = '');
  END IF;
END $$;

