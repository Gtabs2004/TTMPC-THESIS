ALTER TABLE IF EXISTS public.co_makers ADD COLUMN IF NOT EXISTS is_member BOOLEAN NOT NULL DEFAULT false;

DO $$
DECLARE
  existing_pk_name text;
BEGIN
  IF to_regclass('public.co_makers') IS NOT NULL THEN
    -- Drop any existing primary key (name can vary: co_makers_pk / co_makers_pkey / etc.).
    SELECT conname
    INTO existing_pk_name
    FROM pg_constraint
    WHERE conrelid = 'public.co_makers'::regclass
      AND contype = 'p'
    LIMIT 1;

    IF existing_pk_name IS NOT NULL THEN
      EXECUTE format('ALTER TABLE public.co_makers DROP CONSTRAINT %I', existing_pk_name);
    END IF;

    -- Backfill id for existing rows before creating new PK.
    EXECUTE 'UPDATE public.co_makers SET id = DEFAULT WHERE id IS NULL';

    IF NOT EXISTS (
      SELECT 1
      FROM pg_constraint
      WHERE conname = 'co_makers_id_pk'
        AND conrelid = 'public.co_makers'::regclass
    ) THEN
      EXECUTE 'ALTER TABLE public.co_makers ADD CONSTRAINT co_makers_id_pk PRIMARY KEY (id)';
    END IF;

    IF EXISTS (
      SELECT 1
      FROM information_schema.columns
      WHERE table_schema = 'public'
        AND table_name = 'co_makers'
        AND column_name = 'member_id'
        AND is_nullable = 'NO'
    ) THEN
      EXECUTE 'ALTER TABLE public.co_makers ALTER COLUMN member_id DROP NOT NULL';
    END IF;
  END IF;
END $$;

DROP INDEX IF EXISTS idx_co_makers_loan_member_unique;
CREATE INDEX IF NOT EXISTS idx_co_makers_loan_member_idx
ON public.co_makers (loan_id, member_id);

GRANT USAGE ON SCHEMA public TO authenticated;
GRANT SELECT ON public.loan_types TO authenticated;
GRANT SELECT, INSERT ON public.loans TO authenticated;
GRANT SELECT, INSERT, UPDATE ON public.co_makers TO authenticated;

DO $$
BEGIN
  IF to_regclass('public.loan_types') IS NOT NULL THEN
    EXECUTE 'DROP POLICY IF EXISTS "authenticated read loan types" ON loan_types';
    EXECUTE 'CREATE POLICY "authenticated read loan types" ON loan_types FOR SELECT TO authenticated USING (true)';

    EXECUTE 'DROP POLICY IF EXISTS "service role all loan types" ON loan_types';
    EXECUTE 'CREATE POLICY "service role all loan types" ON loan_types FOR ALL USING (auth.role() = ''service_role'') WITH CHECK (auth.role() = ''service_role'')';
  END IF;
END $$;

DO $$
BEGIN
  IF to_regclass('public.loans') IS NOT NULL THEN
    EXECUTE 'DROP POLICY IF EXISTS "authenticated insert own loans" ON loans';
    EXECUTE '
      CREATE POLICY "authenticated insert own loans"
      ON loans
      FOR INSERT
      TO authenticated
      WITH CHECK (
        lower(coalesce(user_email, '''')) = lower(auth.email())
        AND member_id = auth.uid()
      )
    ';

    EXECUTE 'DROP POLICY IF EXISTS "authenticated read own loans" ON loans';
    EXECUTE '
      CREATE POLICY "authenticated read own loans"
      ON loans
      FOR SELECT
      TO authenticated
