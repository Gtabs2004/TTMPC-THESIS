CREATE TABLE IF NOT EXISTS public.koica_loans (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  control_number TEXT NOT NULL UNIQUE,
  full_name TEXT,
  credentials JSONB,
  user_email TEXT,
  loan_type_code TEXT,
  loan_amount NUMERIC,
  principal_amount NUMERIC,
  interest_rate NUMERIC,
  term INTEGER,
  loan_status TEXT NOT NULL DEFAULT 'pending',
  application_status TEXT NOT NULL DEFAULT 'pending',
  application_type TEXT NOT NULL DEFAULT 'new',
  application_date TIMESTAMPTZ NOT NULL DEFAULT now(),
  disbursal_date TIMESTAMPTZ,
  raw_payload JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_koica_loans_user_email ON public.koica_loans (lower(user_email));

DO $$
BEGIN
  ALTER TABLE public.koica_loans ADD COLUMN IF NOT EXISTS full_name TEXT;
  ALTER TABLE public.koica_loans ADD COLUMN IF NOT EXISTS credentials JSONB;

  IF EXISTS (
    SELECT 1
    FROM information_schema.columns
    WHERE table_schema = 'public'
      AND table_name = 'koica_loans'
      AND column_name = 'user_id'
  ) THEN
    ALTER TABLE public.koica_loans DROP COLUMN IF EXISTS user_id;
  END IF;

  IF EXISTS (
    SELECT 1
    FROM information_schema.columns
    WHERE table_schema = 'public'
      AND table_name = 'koica_loans'
      AND column_name = 'member_id'
  ) THEN
    ALTER TABLE public.koica_loans DROP COLUMN IF EXISTS member_id;
  END IF;

  IF EXISTS (
    SELECT 1
    FROM information_schema.columns
    WHERE table_schema = 'public'
      AND table_name = 'koica_loans'
      AND column_name = 'user_email'
      AND is_nullable = 'NO'
  ) THEN
    ALTER TABLE public.koica_loans ALTER COLUMN user_email DROP NOT NULL;
  END IF;
END $$;

